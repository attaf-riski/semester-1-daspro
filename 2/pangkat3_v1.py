# Deskripsi : menghitung nilai pangkat 3 dari sebuah masukan integer

# Definisi dan spesifikasi dari fungsi pangkat 3 bernama fx3v1(x) adalah:
# fx3v1 : integer --> integer
#   fx3v1(x) menghitung pangkat tiga dari x, sebuah bilangan integer

# Realisasi
def fx3v1(x) :
    return (x*x*x)

# Aplikasi
print(fx3v1(4)) # hasilnya 64
print(fx3v1(10)) # hasilnya 1000
